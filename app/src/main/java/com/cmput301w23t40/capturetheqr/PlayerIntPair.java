package com.cmput301w23t40.capturetheqr;

public class PlayerIntPair {
    private Player player;
    private int integer;

    public PlayerIntPair(Player player, int integer) {
        this.player = player;
        this.integer = integer;
    }

    public Player getPlayer() {
        return player;
    }

    public int getInteger() {
        return integer;
    }
}
